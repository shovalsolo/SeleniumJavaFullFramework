/* 
 * This class is showing the use of
 * A boolean variable takes only true of false
 */

package dataTypeDemo;

public class VariablesDemo {
	
	public static void main (String[] args) {
		
		boolean status; 						// A boolean variable takes only true of false
		status=true;							// Setting true to status variable
		
		boolean newstatus = false;				// Setting false to status variable
		
		System.out.println("Status is: "+ status + "  -and-  "+"Newstatus is: "+ newstatus);	//printing the content of the variables
	}

}

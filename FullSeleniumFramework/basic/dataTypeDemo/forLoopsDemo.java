package dataTypeDemo;

public class forLoopsDemo {
	
	public static void main(String[] args) {
		System.out.println("Loop Start");
		
		for (int i = 1 ; i <= 20 ; i++) 
		{
			System.out.println("I values are " + i);
		}
		System.out.println("Loop End");
	}
}

/* 
 * This class is showing about inheritance
 * The child classes will inherit the add and sub functions
 */

package inheritanceDemo;

public class BaseClass {
	
	public void add() {
		System.out.println("Hey I'm from base class and the addition is 250");
	}
	
	public void sub() {
		System.out.println("Hey I'm from base class and the subtraction is 150");
	}

}
